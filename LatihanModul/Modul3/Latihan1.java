package Modul3;

public class Latihan1 {
    public static void main(String[] args) {
        int pertama = 20;
        int kedua = 7;

        int tambah = pertama + kedua;
        int kurang = pertama - kedua;
        int kali = pertama * kedua;
        int bagi = pertama / kedua;
        int sisa = pertama % kedua;

        System.out.println(tambah + "\n" + kurang + "\n" + kali + "\n" + bagi + "\n" + sisa);
    }
}
